export default (state, action) => {
  switch (action.type) {
    case "DELETE_TRANSACTION":
      return {
        ...state,
        transactions: state.transactions.filter(
          (transaction) => transaction.id !== action.palyload
        ),
      };
    //先确定基本的情况，也就是暂时没有type的情况，
    default:
      return state; //直接返回state。
  }
};
